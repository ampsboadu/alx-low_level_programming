/**
 * add -add two numbers
 * @a: first number
 * @b: second number
 * Return: a+b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub -substract number from number
 * @a: first number
 * @b: second number
 * Return: a-b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul -multiply two numbers
 * @a: fisrt numebr
 * @b: second number
 * Return: a*b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div -divide number
 * @a: first number
 * @b: second number
 * Return: a/b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod -modulo of number
 * @a: first number
 * @b: second number
 * Return: a%b
 */
int mod(int a, int b)
{
	return (a % b);
}
